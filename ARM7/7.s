AREA NCR,CODE,READONLY
DIVISOR RN 1
DIVIDEND RN 2
QUOTIENT RN 3
REMAINDER RN 4
FAC RN 5
NUM RN 8
ENTRY
	MOV R6,#10
	MOV R7,#6
	MOV R9,#4
	
	MOV NUM,R6
	BL FACTORIAL
	MOV R6,FAC
	
	MOV NUM,R7
	BL FACTORIAL
	MOV R7,FAC
	
	MOV NUM,R9
	BL FACTORIAL
	MOV R9,FAC
	
	MUL R7,R9,R7
	MOV DIVIDEND,R6
	MOV DIVISOR,R7
	
	BL DIVIDE
STOP B STOP
FACTORIAL
	MOV FAC,#1
FACLOOP
	MUL FAC,NUM,FAC
	SUBS NUM,NUM,#1
	BNE FACLOOP
	MOV PC,LR
DIVIDE
	MOV QUOTIENT,#0
DIVLOOP
	SUBS DIVIDEND,DIVIDEND,DIVISOR
	ADDPL QUOTIENT,QUOTIENT,#1
	BPL DIVLOOP
	ADDMI REMAINDER,DIVIDEND,DIVISOR
	MOV PC,LR
	END